import { expect, test } from "@playwright/test";
import { cleanupNodeVisualData, NODE_VISUAL_DISTRIBUTED_PROJECT_ID, NODE_VISUAL_NAME, seedNodeVisualData } from "./helpers/devData";
import { cleanupTestProject, createTestProject } from "./unit/helpers/testProject";
import { prisma } from "../src/lib/prisma";
import { goto } from "./helpers/navigation";

// These specs mutate an isolated fixture node/project only - see the
// isolation guard (assertFixtureDatabase) seedNodeVisualData/
// cleanupNodeVisualData both call first.
test.beforeEach(async () => {
  await seedNodeVisualData();
});
test.afterEach(async () => {
  await cleanupNodeVisualData();
});
test.afterAll(async () => {
  await prisma.$disconnect();
});

// CaptureSource ids are freshly generated by every seedNodeVisualData() call
// (no fixed id to import), and CaptureSource has no nodeId column - so the
// only reliable way to find "this test run's" CaptureSource for a named
// fixture camera is through that camera's own NodeCamera row, scoped to the
// freshly-seeded node. Looking a CaptureSource up by name alone is NOT safe:
// a CaptureSource from an earlier, already-cleaned-up run can still be
// sitting in a reused fixture database under the same name.
async function captureSourceForCamera(cameraName: string) {
  const node = await prisma.plantLabNode.findUniqueOrThrow({ where: { name: NODE_VISUAL_NAME } });
  const camera = await prisma.nodeCamera.findFirstOrThrow({ where: { nodeId: node.id, name: cameraName } });
  return prisma.captureSource.findUniqueOrThrow({ where: { id: camera.captureSourceId! } });
}

test.describe("project creation with a distributed CaptureSource", () => {
  test("selecting a remote camera and sensors links them without a raw device path", async ({ page }) => {
    const wideSource = await captureSourceForCamera("Greenhouse Wide");
    const node = await prisma.plantLabNode.findUniqueOrThrow({ where: { name: NODE_VISUAL_NAME } });
    const outsideSensor = await prisma.nodeSensor.findUniqueOrThrow({ where: { nodeId_key: { nodeId: node.id, key: "greenhouse-outside" } } });
    const middleSensor = await prisma.nodeSensor.findUniqueOrThrow({ where: { nodeId_key: { nodeId: node.id, key: "greenhouse-middle" } } });

    await goto(page, "/");
    await page.getByLabel("Name").fill("Distributed Creation Test");
    await page.getByTestId(`capture-source-option-${wideSource.id}`).locator('input[type="radio"]').check();
    await page.getByTestId(`project-sensor-option-${outsideSensor.id}`).locator('input[type="checkbox"]').check();
    await page.getByTestId(`project-sensor-option-${middleSensor.id}`).locator('input[type="checkbox"]').check();

    await page.getByRole("button", { name: "Create Project" }).click();
    await page.waitForURL(/\/projects\/[^/]+$/);

    const projectId = page.url().split("/projects/")[1];
    try {
      const viewport = await prisma.projectViewport.findFirst({ where: { projectId, active: true }, include: { captureSource: true } });
      expect(viewport).not.toBeNull();
      // Bound via captureSourceId, never a raw /dev/video* path.
      expect(viewport?.captureSourceId).toBe(wideSource.id);
      const project = await prisma.project.findUniqueOrThrow({ where: { id: projectId } });
      expect(project.cameraDevice).toBeNull();

      const bindings = await prisma.projectSensorBinding.findMany({ where: { projectId, enabled: true }, include: { sensor: true } });
      expect(bindings.map((binding) => binding.sensor.key).sort()).toEqual(["greenhouse-middle", "greenhouse-outside"]);
    } finally {
      await cleanupTestProject(prisma, projectId);
    }
  });

  test("an unavailable camera is visible for context but cannot be selected", async ({ page }) => {
    const doorSource = await captureSourceForCamera("Greenhouse Door");
    await goto(page, "/");
    const doorOption = page.getByTestId(`capture-source-option-${doorSource.id}`);
    await expect(doorOption).toBeVisible();
    await expect(doorOption.locator('input[type="radio"]')).toBeDisabled();
    await expect(doorOption.getByText("Unavailable")).toBeVisible();
  });
});

