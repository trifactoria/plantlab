test.describe("project settings", () => {
  test("shows Capture Source mode with a link to the source, and manages sensor bindings", async ({ page }) => {
    await goto(page, `/projects/${NODE_VISUAL_DISTRIBUTED_PROJECT_ID}/settings`);

    await expect(page.getByRole("radio", { name: "Capture Source" })).toBeChecked();
    await expect(page.getByText("Capture schedule controlled by the selected Capture Source.")).toBeVisible();
    await expect(page.getByRole("link", { name: "View Capture Source" })).toBeVisible();

    const binding = await prisma.projectSensorBinding.findFirstOrThrow({ where: { label: "Middle Shelf" } });
    const middleRow = page.getByTestId(`settings-sensor-binding-${binding.id}`);
    await expect(middleRow).toBeVisible();
    await middleRow.getByRole("button", { name: "Unlink" }).click();
    await expect(page.getByText(/Unlinked \/ historical/)).toBeVisible();

    const afterUnlink = await prisma.projectSensorBinding.findUniqueOrThrow({ where: { id: binding.id } });
    expect(afterUnlink.enabled).toBe(false);
    expect(afterUnlink.unlinkedAt).not.toBeNull();

    await page.getByText(/Unlinked \/ historical/).click();
    await page.getByTestId(`settings-sensor-binding-${binding.id}`).getByRole("button", { name: "Relink" }).click();
    await expect(page.getByTestId(`settings-sensor-binding-${binding.id}`).getByRole("button", { name: "Unlink" })).toBeVisible();
    const relinked = await prisma.projectSensorBinding.findUniqueOrThrow({ where: { id: binding.id } });
    expect(relinked.enabled).toBe(true);
    expect(relinked.unlinkedAt).toBeNull();
  });
});

test.describe("photo environment card", () => {
  test("shows the nearest reading per linked sensor within the match window", async ({ page }) => {
    await goto(page, `/photos/${NODE_VISUAL_DISTRIBUTED_PROJECT_ID}-photo`);
    const card = page.getByTestId("photo-environment-card");
    await expect(card).toBeVisible();
    await expect(card.getByText("Outside", { exact: true })).toBeVisible();
    await expect(card.getByText("Middle Shelf")).toBeVisible();
    // Both linked sensors' last reading lands at the same fixture timestamp,
    // so both match the same distance - assert at least one "Matched
    // within" note rather than requiring a single unique match.
    await expect(card.getByText(/Matched within \d+ seconds/).first()).toBeVisible();
  });
});

test.describe("existing direct-local project compatibility", () => {
  test("a legacy direct-local project keeps its raw device selection editable in settings", async ({ page }) => {
    const project = await createTestProject(prisma, { name: "Legacy Direct-Local Project", cameraDevice: "/dev/video-e2e-direct" });
    try {
      await goto(page, `/projects/${project.id}/settings`);
      await expect(page.getByRole("radio", { name: "Direct Local" })).toBeChecked();
      await expect(page.locator('select[name="cameraDevice"]')).toBeVisible();
    } finally {
      await cleanupTestProject(prisma, project.id, project.localPhotoDirectory);
    }
  });
});
